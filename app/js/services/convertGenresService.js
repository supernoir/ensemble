const convertValueToOptionsMap = (val, key) => {
	return {
		key,
		text : val,
		value: val.toLowerCase().replace(/\s/g, '')
	};
};

const convertGenresToOptionsMap = (data) => {
	return data !== void 0 && data.length > 0
		? data.map((val,key)=> {
			return convertValueToOptionsMap(val, key);
		})
		: [];
};

let result = convertGenresToOptionsMap(data);
fs.writeFileSync('./genres.json', JSON.stringify(result));